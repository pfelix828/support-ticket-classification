@dataclass
class SupportTicket:
    id: int
    text: str
    category: str
    secondary_category: str | None
    customer_tier: str
    account_age_days: int
    previous_tickets: int
    is_ambiguous: bool
    urgency: str


def generate_ticket(ticket_id: int, templates_by_category: dict, force_ambiguous: bool = False) -> SupportTicket:
    """Generate a single ticket using paraphrased templates."""
    is_ambiguous = force_ambiguous or random.random() < 0.10

    if is_ambiguous:
        ambig = random.choice(AMBIGUOUS_TEMPLATES)
        text = random.choice(ambig["texts"])
        category = ambig["category"]
        secondary_category = ambig["secondary_category"]
        urgency = random.choice(["medium", "high"])
    else:
        categories = list(CATEGORY_WEIGHTS.keys())
        weights = [CATEGORY_WEIGHTS[c] for c in categories]
        category = random.choices(categories, weights=weights, k=1)[0]

        cat_data = templates_by_category.get(category, {"normal": [], "hard": []})
        # 70% of non-ambiguous tickets use hard paraphrases (if available)
        if cat_data["hard"] and random.random() < 0.70:
            text, urgency = random.choice(cat_data["hard"])
        elif cat_data["normal"]:
            text, urgency = random.choice(cat_data["normal"])
        else:
            text = f"I have an issue with {category}"
            urgency = "medium"

        secondary_category = None

    # Generic opener (25% of tickets) — obscures category signal
    if random.random() < 0.25:
        text = random.choice(GENERIC_OPENERS) + text[0].lower() + text[1:]

    # Cross-category noise (30% of tickets) — adds red-herring signals
    if random.random() < 0.30:
        text += " " + random.choice(CROSS_CATEGORY_NOISE)

    # Context suffix (20% of tickets)
    if random.random() < 0.20:
        text += random.choice(CONTEXT_SUFFIXES)

    # Tone prefix based on urgency
    if urgency == "high" and random.random() < 0.35:
        text = random.choice(URGENT_PREFIXES) + text
    elif urgency == "low" and random.random() < 0.20:
        text = random.choice(CASUAL_PREFIXES) + text

    # Metadata
    customer_tier = random.choices(CUSTOMER_TIERS, weights=TIER_WEIGHTS, k=1)[0]
    account_age_days = int(random.gammavariate(2, 180))
    previous_tickets = int(random.expovariate(0.5))

    return SupportTicket(
        id=ticket_id,
        text=text.strip(),
        category=category,
        secondary_category=secondary_category,
        customer_tier=customer_tier,
        account_age_days=min(account_age_days, 1500),
        previous_tickets=min(previous_tickets, 20),
        is_ambiguous=is_ambiguous,
        urgency=urgency,
    )


def generate_dataset(n: int = 100_000) -> list[dict]:
    templates = load_paraphrased_templates()

    total_normal = sum(len(v["normal"]) for v in templates.values())
    total_hard = sum(len(v["hard"]) for v in templates.values())
    print(f"Loaded {total_normal + total_hard} templates ({total_normal} normal + {total_hard} hard) across {len(templates)} categories")

    tickets = []
    for i in range(n):
        ticket = generate_ticket(i + 1, templates)
        tickets.append(asdict(ticket))
    return tickets


def get_category_stats(tickets):
    from collections import Counter
    counts = Counter(t["category"] for t in tickets)
    total = len(tickets)
    stats = {}
    for cat, count in counts.most_common():
        stats[cat] = {
            "count": count,
            "percentage": round(count / total * 100, 1),
        }
    return stats


def main():
    n = 100_000
    print(f"Generating {n:,} tickets with paraphrased templates...")
    tickets = generate_dataset(n)

    stats = get_category_stats(tickets)
    print("\nCategory distribution:")
    for cat, info in stats.items():
        print(f"  {cat:25s} {info['count']:6d}  ({info['percentage']}%)")

    ambiguous = sum(1 for t in tickets if t["is_ambiguous"])
    unique = len(set(t["text"] for t in tickets))
    print(f"\nAmbiguous: {ambiguous:,} ({ambiguous/len(tickets)*100:.1f}%)")
    print(f"Unique texts: {unique:,} / {len(tickets):,} ({unique/len(tickets)*100:.1f}%)")

    # Save
    output_dir = DATA_DIR
    with open(output_dir / "tickets.json", "w") as f:
        json.dump(tickets, f)
    size_mb = (output_dir / "tickets.json").stat().st_size / 1024 / 1024
    print(f"\nExported to tickets.json ({size_mb:.1f} MB)")

    sample = random.sample(tickets, 1000)
    with open(output_dir / "tickets_sample.json", "w") as f:
        json.dump(sample, f, indent=2)

    with open(output_dir / "category_stats.json", "w") as f:
        json.dump(stats, f, indent=2)

    print("Done!")


if __name__ == "__main__":
    main()
