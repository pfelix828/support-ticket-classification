# --- Load paraphrased templates ---

def load_paraphrased_templates():
    """Load both normal and hard LLM-paraphrased templates."""
    # Normal paraphrases
    path = DATA_DIR / "paraphrased_templates.json"
    with open(path) as f:
        normal = json.load(f)

    # Hard paraphrases (deliberately ambiguous)
    hard_path = DATA_DIR / "hard_paraphrases.json"
    hard = []
    if hard_path.exists():
        with open(hard_path) as f:
            hard = json.load(f)

    # Organize by category — mix normal and hard
    by_category = {}
    for t in normal:
        cat = t["category"]
        urgency = t["urgency"]
        if cat not in by_category:
            by_category[cat] = {"normal": [], "hard": []}
        for p in t["paraphrases"]:
            by_category[cat]["normal"].append((p, urgency))

    for t in hard:
        cat = t["category"]
        urgency = t["urgency"]
        if cat not in by_category:
            by_category[cat] = {"normal": [], "hard": []}
        for p in t.get("hard_paraphrases", []):
            by_category[cat]["hard"].append((p, urgency))

    normal_count = sum(len(v["normal"]) for v in by_category.values())
    hard_count = sum(len(v["hard"]) for v in by_category.values())
    print(f"  Normal paraphrases: {normal_count}")
    print(f"  Hard paraphrases: {hard_count}")

    return by_category


